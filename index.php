          <h3 id="what"><b><big><big>What is a Disaster</big></big><b></h3>

          <p id="d-what"><i>A disaster is a serious disruption, occurring over a relatively short time, of the functioning of a community or a society involving widespread human, material, economic or environmental loss and impacts, which exceeds the ability of the affected community or society to cope using its own resources.

                        In contemporary, disasters are seen as the consequence of inappropriately managed risk. These risks are the product of a combination of both hazards and vulnerability. Hazards that strike in areas with low vulnerability will never become disasters, as in the case of uninhabited regions.

                        Developing countries suffer the greatest costs when a disaster hits more than 95% of all deaths caused by hazards occur in developing countries, and losses due to natural hazards are 20 times greater (as a percentage of GDP) in developing countries than in industrialized countries.</i></p>

            <h3 id="importance"><b><big><big>The Importance of Disaster Management</big></big><b></h3>

            <p id="d-importance"><i>The purpose of this system is to study and explore the importance of reports in natural disaster events and to identify the impacts to people in natural disaster events. A disaster is an unforeseen event, which can overwhelm the capacity of the affected people to manage its impact. 
                                 Many people are periodically exposed to natural disasters in their life, and most disasters, or more correctly hazards that lead to disasters, cannot be prevented. However, their effects can be mitigated. Disaster management efforts aim to reduce or avoid the potential losses from hazards, 
                                 assure prompt and appropriate assistance to the victims of a disaster, and achieve a rapid and effective recovery. It is crucial that people remain safe during and after disasters. Health facilities at all levels deserve special attention in the case of natural disasters 
                                 as they must continue the work of current patient treatment within their facilities and provide additional care for persons injured by the disaster event. Disaster management becomes even more important for hospitals as the health sector has been particularly vulnerable to the damage caused. 
                                 For this system, secondary information was retrieved from the Internet on sudden-onset natural disasters in different parts of the world was collected. This system found some barriers and deliverables for disaster managers that could mitigate the risk of a natural disaster’s impact on people. 
                                 Accordingly, this system evaluates the importance of disaster management for Malaysian citizen and the challenges that need to be considered during the disaster response. </i></p>

            <h3 id="headsup"><b><big><big>What is Malaysia Prihatin</big></big><b></h3>

            <p id="d-headsup"><i>Malaysia Prihatin is a disaster risk management system that typically organised along five stages including prevention, protection, preparedness, response, recovery and review. Territorial and urban planning, as well as land management, play an important role in risk prevention. 
                              For example, limiting the development in flood prone areas, and by encouraging flood and drought risk-sensitive land use and management practices. With respect to water management, disaster risk management entails identification of areas prone to 
                              natural hazard of different intensity and frequency, and implementation of protection measures, both structural and non-structural, aimed to reduce the exposure and/or vulnerability to strikes of ‘capricious nature’. </i></p>
